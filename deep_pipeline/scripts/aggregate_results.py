#!/usr/bin/env python3
"""Aggregate all experiment results into a markdown summary table.

Reads experiments/results/<id>_<task>.json files.
Writes experiments/results_summary.md.
"""
import json
from pathlib import Path

REPO = Path(__file__).resolve().parent.parent
RESULTS_DIR = REPO / "experiments" / "results"

EXPERIMENT_NAMES = {
    "c1_focal": "C1: Focal Loss",
    "c3_cosine_fid": "C3: Cosine Fidelity",
    "a3_padding_fid": "A3: Padding-Aware Fidelity",
    "a1_var_batching": "A1: Variable-Length Batching",
    "a4_truncate": "A4: Truncate-and-Pack",
    "c2_gradnorm": "C2: GradNorm Weighting",
    "a2_chunking": "A2: Sequence Chunking",
    "b1_hidden_mmd": "B1: Hidden-State MMD",
    "b3_knn": "B3: kNN Translation",
    "b5_ot": "B5: Optimal Transport",
    "b6_dann": "B6: DANN Adversarial",
    "b4_contrastive": "B4: Contrastive Alignment",
    "b2_shared_enc": "B2: Shared Encoder",
}

EXPERIMENT_ORDER = [
    "c1_focal", "c3_cosine_fid", "a3_padding_fid", "a1_var_batching",
    "a4_truncate", "c2_gradnorm", "a2_chunking", "b1_hidden_mmd",
    "b3_knn", "b5_ot", "b6_dann", "b4_contrastive", "b2_shared_enc",
]


def load_result(exp_id: str, task: str) -> dict | None:
    path = RESULTS_DIR / f"{exp_id}_{task}.json"
    if not path.exists():
        return None
    with open(path) as f:
        return json.load(f)


def fmt_delta(result: dict | None, metric: str) -> str:
    if result is None:
        return "N/A"
    if result.get("status") != "ok":
        return result.get("status", "error")
    diff = result.get("difference", {})
    val = diff.get(metric)
    if val is None:
        return "N/A"
    return f"{val:+.4f}"


def main():
    lines = [
        "# Experiment Results: A/B/C Recommendations",
        "",
        "| # | Experiment | Sepsis AUCROC Δ | Sepsis AUCPR Δ | Mortality AUCROC Δ | Mortality AUCPR Δ | Status |",
        "|---|---|---|---|---|---|---|",
        "| 0 | Baseline (prev best) | +0.0059 | - | +0.0264 | +0.0296 | reference |",
    ]

    for i, exp_id in enumerate(EXPERIMENT_ORDER, 1):
        name = EXPERIMENT_NAMES.get(exp_id, exp_id)
        sep = load_result(exp_id, "sepsis")
        mort = load_result(exp_id, "mortality")

        sep_auroc = fmt_delta(sep, "AUCROC")
        sep_aucpr = fmt_delta(sep, "AUCPR")
        mort_auroc = fmt_delta(mort, "AUCROC")
        mort_aucpr = fmt_delta(mort, "AUCPR")

        # Determine status
        statuses = set()
        for r in [sep, mort]:
            if r is None:
                statuses.add("not_run")
            else:
                statuses.add(r.get("status", "unknown"))
        status = ", ".join(sorted(statuses))

        lines.append(f"| {i} | {name} | {sep_auroc} | {sep_aucpr} | {mort_auroc} | {mort_aucpr} | {status} |")

    lines.append("")
    lines.append("Generated by `scripts/aggregate_results.py`")
    lines.append("")

    out_path = REPO / "experiments" / "results_summary.md"
    out_path.parent.mkdir(parents=True, exist_ok=True)
    out_path.write_text("\n".join(lines))
    print(f"Summary written to {out_path}")

    # Also print to stdout
    print()
    for line in lines:
        print(line)


if __name__ == "__main__":
    main()
